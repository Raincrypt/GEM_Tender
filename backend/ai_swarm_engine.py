async def _compliance_sentinel(bus: SwarmBus, ctx: dict) -> None:
    """Policy Guardian Γ: Validates document text against mandatory GEM/CVC procurement policies."""
    await asyncio.sleep(0.1)

    doc_text = ctx.get("document_text", "")
    tender_title = ctx.get("tender_title", "Document")
    vendor_profile = ctx.get("vendor_profile", {})

    doc_lower = doc_text.lower()

    # ── 1. Check mandatory clause presence ──────────────────────
    present_clauses = []
    missing_clauses = []

    for clause in _MANDATORY_GEM_CLAUSES:
        found = any(kw in doc_lower for kw in clause["keywords"])
        if found:
            present_clauses.append(clause)
        else:
            missing_clauses.append(clause)

    # ── 2. MSME eligibility check ────────────────────────────────
    is_msme = vendor_profile.get("msme_registered") or vendor_profile.get("msme")
    annual_turnover = vendor_profile.get("annual_turnover_cr") or vendor_profile.get("turnover")
    msme_eligible_waivers = []

    if is_msme:
        msme_eligible_waivers.append("Prior experience requirement waiver (MSME Order 2012)")
        if annual_turnover and float(annual_turnover) < 500:
            msme_eligible_waivers.append("Annual turnover requirement waiver (MSME Order 2012)")
        msme_eligible_waivers.append("EMD exemption eligible (MSME Procurement Policy)")

    # ── 3. CVC compliance flags ──────────────────────────────────
    cvc_flags = []
    if "single bid" in doc_lower or "single vendor" in doc_lower:
        cvc_flags.append("SINGLE-BID WARNING: Single vendor/bid clause detected — CVC advises re-tender")
    if "nomination" in doc_lower and "proprietary" not in doc_lower:
        cvc_flags.append("NOMINATION BASIS: Nominations without proprietary justification violate CVC norms")
    if "advance payment" in doc_lower and "bank guarantee" not in doc_lower:
        cvc_flags.append("ADVANCE PAYMENT WITHOUT BG: Advance payments without bank guarantee are non-compliant")

    # ── 4. Compute compliance score ──────────────────────────────
    total_clauses = len(_MANDATORY_GEM_CLAUSES)
    compliance_pct = round((len(present_clauses) / total_clauses) * 100, 1)
    compliance_status = (
        "FULLY COMPLIANT"   if compliance_pct >= 90 else
        "MOSTLY COMPLIANT"  if compliance_pct >= 70 else
        "PARTIALLY COMPLIANT" if compliance_pct >= 50 else
        "NON-COMPLIANT"
    )

    # ── 5. LLM narrative summary ─────────────────────────────────
    missing_names = [c["name"] for c in missing_clauses]
    default_text = (
        f"Policy Guardian Γ — Compliance Assessment for '{tender_title}': "
        f"Score {compliance_pct:.0f}% ({len(present_clauses)}/{total_clauses} mandatory clauses found). "
        f"Status: {compliance_status}. "
        + (f"Missing clauses: {', '.join(missing_names[:3])}. " if missing_names else "All mandatory clauses present. ")
        + (f"CVC flags: {'; '.join(cvc_flags[:2])}." if cvc_flags else "No CVC violations detected.")
    )

    system = "You are Policy Guardian Γ, a government procurement compliance AI."
    user = (
        f"Compliance scan for tender '{tender_title}'. "
        f"Score: {compliance_pct:.0f}% ({len(present_clauses)} of {total_clauses} GEM mandatory clauses present). "
        f"Missing: {', '.join(missing_names) if missing_names else 'None'}. "
        f"CVC flags: {'; '.join(cvc_flags) if cvc_flags else 'None'}. "
        f"Write a formal compliance advisory note (3 sentences max)."
    )

    text = _call_agent_llm("COMPLIANCE", system, user, default_text)
    bus.publish(AgentMessage(
        "COMPLIANCE", text, "consensus", 0.97,
        {
            "phase": "compliance_scan",
            "compliance_score_pct": compliance_pct,
            "compliance_status": compliance_status,
            "present_clauses": [c["id"] for c in present_clauses],
            "missing_clauses": [{"id": c["id"], "name": c["name"], "cvc_ref": c["cvc_ref"]} for c in missing_clauses],
            "cvc_flags": cvc_flags,
            "msme_eligible_waivers": msme_eligible_waivers,
            "is_msme_vendor": bool(is_msme),
        }
    ))


