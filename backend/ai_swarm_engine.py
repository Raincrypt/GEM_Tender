class AgentMessage:
    def __init__(self, sender, content, msg_type="thought", confidence=0.0, metadata=None):
        self.id = hashlib.md5(f"{sender}{content}{datetime.now(timezone.utc).replace(tzinfo=None).isoformat()}".encode()).hexdigest()[:12]
        self.sender = sender
        self.content = content
        self.msg_type = msg_type
        self.confidence = confidence
        self.metadata = metadata or {}
        self.timestamp = datetime.now(timezone.utc).replace(tzinfo=None)
    def to_dict(self):
        return {"id":self.id,"sender":self.sender,"agent_info":AGENT_REGISTRY.get(self.sender,{}),"content":self.content,
                "type":self.msg_type,"confidence":self.confidence,"metadata":self.metadata,"timestamp":self.timestamp.isoformat()}

class SwarmBus:
    def __init__(self):
        self.messages: List[AgentMessage] = []
    def publish(self, msg): self.messages.append(msg)
    def history(self): return [m.to_dict() for m in self.messages]
    def last(self, agent):
        for m in reversed(self.messages):
            if m.sender == agent: return m
        return None

def _call_agent_llm(sender: str, system_prompt: str, user_prompt: str, default_text: str) -> str:
    """Helper to query unified LLM with graceful offline fallback."""
    try:
        import llm_client
        # Add strict formatting rules to system prompt for local LLMs (like llama3) to prevent conversational preambles or prefixes.
        enhanced_system = (
            f"{system_prompt}\n"
            f"IMPORTANT: You must return ONLY the direct response content. Do NOT include any preamble, intro, conversational filler (such as 'Here is...', 'Certainly!'), "
            f"markdown code blocks, or prefix your response with the agent's name or title (e.g., do NOT start with '{sender}:' or similar)."
        )
        res = llm_client.generate_text(user_prompt, system_instruction=enhanced_system, temperature=0.3)
        if res and len(res.strip()) > 15:
            clean_res = res.strip()
            # Remove markdown code fences if generated by the LLM
            if clean_res.startswith("```"):
                lines = clean_res.splitlines()
                if lines[0].startswith("```"):
                    lines = lines[1:]
                if lines and lines[-1].strip() == "```":
                    lines = lines[:-1]
                clean_res = "\n".join(lines).strip()
            
            # Clean up potential LLM prefixing (e.g. "Planner Ω: ")
            for prefix in [sender, f"{sender} Ω:", f"{sender} Σ:", f"{sender} Δ:", f"{sender} Ξ:", f"{sender} Λ:", f"{sender} Ψ:", f"{sender} Φ:", "PLANNER:", "ANALYST:", "NEGOTIATOR:", "CRITIC:", "AUDITOR:", "SENTINEL:", "ORACLE:"]:
                if clean_res.upper().startswith(prefix.upper()):
                    clean_res = clean_res[len(prefix):].strip()
            
            # Remove leading conversational phrases often output by local LLMs
            prefixes_to_remove = ["HERE IS ", "CERTAINLY! ", "SURE, ", "CERTAINLY, ", "HERE'S "]
            for pfx in prefixes_to_remove:
                if clean_res.upper().startswith(pfx):
                    clean_res = clean_res[len(pfx):].strip()
                    
            if clean_res.startswith('"') and clean_res.endswith('"'):
                clean_res = clean_res[1:-1].strip()
            if clean_res.startswith("'") and clean_res.endswith("'"):
                clean_res = clean_res[1:-1].strip()
                
            return clean_res.strip()
    except Exception:
        pass
    return default_text

