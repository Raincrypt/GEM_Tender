async def _coi_sentinel(bus: SwarmBus, ctx: dict) -> None:
    """Kinship Sentinel X: Scans for conflict of interest and collusion links between bidders."""
    await asyncio.sleep(0.1)
    bids_data = ctx.get("bids_data", [])
    
    overlaps = []
    has_high_risk = False
    has_med_risk = False
    
    def get_domain(email):
        if email and "@" in email:
            return email.split("@")[1].lower().strip()
        return ""
    
    # Compare each pair of vendors
    for i in range(len(bids_data)):
        for j in range(i + 1, len(bids_data)):
            v1 = bids_data[i]
            v2 = bids_data[j]
            
            n1 = v1.get("vendor_name") or v1.get("company_name") or "Vendor A"
            n2 = v2.get("vendor_name") or v2.get("company_name") or "Vendor B"
            links = []
            score = 0
            
            # 1. IP Check
            ip1, ip2 = v1.get("ip"), v2.get("ip")
            if ip1 and ip2:
                if ip1 == ip2:
                    links.append("Identical IP Address")
                    score += 4
                elif ip1.rsplit(".", 1)[0] == ip2.rsplit(".", 1)[0]:
                    links.append("Shared IP Subnet (/24)")
                    score += 2
            
            # 2. Email Domain Check
            d1, d2 = get_domain(v1.get("email")), get_domain(v2.get("email"))
            if d1 and d2 and d1 == d2:
                if d1 not in ["gmail.com", "yahoo.com", "outlook.com", "hotmail.com", "mail.com"]:
                    links.append(f"Matching Email Domain ({d1})")
                    score += 3
            
            # 3. Directors Check
            dirs1 = set(v1.get("directors", []) or [])
            dirs2 = set(v2.get("directors", []) or [])
            shared_dirs = dirs1.intersection(dirs2)
            if shared_dirs:
                links.append(f"Shared Directors ({', '.join(shared_dirs)})")
                score += 4
                
            # 4. Address Check
            addr1, addr2 = v1.get("address"), v2.get("address")
            if addr1 and addr2 and addr1.strip().lower() == addr2.strip().lower():
                links.append("Matching Registered Address")
                score += 4
                
            # 5. Phone Check
            ph1, ph2 = v1.get("phone"), v2.get("phone")
            if ph1 and ph2 and str(ph1).strip() == str(ph2).strip():
                links.append("Matching Phone Number")
                score += 3
                
            if links:
                level = "HIGH" if score >= 5 else "MEDIUM" if score >= 2 else "LOW"
                if level == "HIGH": has_high_risk = True
                elif level == "MEDIUM": has_med_risk = True
                overlaps.append({
                    "vendor_a": n1,
                    "vendor_b": n2,
                    "links": links,
                    "score": score,
                    "level": level
                })
                
    overall_status = "CRITICAL" if has_high_risk else "WARNING" if has_med_risk else "CLEAR"
    
    # Query LLM for assessment narrative
    default_text = (
        f"Kinship Sentinel X Analysis Complete. Status: {overall_status}. "
        f"Found {len(overlaps)} overlap pairs between bidders. "
        + (" | ".join([f"{o['vendor_a']} & {o['vendor_b']} ({o['level']}): {', '.join(o['links'])}" for o in overlaps]) if overlaps else "No overlapping metadata found.")
    )
    
    system = "You are Kinship Sentinel X, cognitive fraud auditor. Detects conflicts of interest and bidder links."
    user = (
        f"Analyze this bidder overlap metadata:\n"
        f"Bidders scanned: {[v.get('vendor_name') or v.get('company_name') for v in bids_data]}\n"
        f"Overlaps detected: {json.dumps(overlaps)}\n"
        f"Overall risk assessment: {overall_status}.\n"
        f"Write a concise executive forensic report (2-3 sentences) warning of potential collusion rings, conflicts of interest, or certifying the bid group clean."
    )
    
    text = _call_agent_llm("CONFLICT_OF_INTEREST", system, user, default_text)
    bus.publish(AgentMessage("CONFLICT_OF_INTEREST", text, "alert" if overlaps else "thought", 0.90, {
        "phase": "conflict_of_interest",
        "overall_status": overall_status,
        "overlaps": overlaps,
        "conflict_count": len(overlaps)
    }))

